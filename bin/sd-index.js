#!/usr/bin/env node

/**
 * sd-index.js — Generate session-decisions-index.yaml from session-decisions.md
 *
 * Defence in depth: even if an agent ignores the "last 30 lines" instruction
 * in dead-reckoning.md, this index file is what they should read on boot.
 * Small, structured, current.
 *
 * Usage: node bin/sd-index.js
 * Output: docs/internal/session-decisions-index.yaml
 */

import { readFileSync, writeFileSync } from 'fs';

const SD_FILE = 'docs/internal/session-decisions.md';
const INDEX_FILE = 'docs/internal/session-decisions-index.yaml';

const content = readFileSync(SD_FILE, 'utf-8');
const lines = content.split('\n');

// Extract SD rows from markdown table
const sdRows = [];
for (const line of lines) {
  const match = line.match(/^\|\s*SD-(\d+)\s*\|\s*\[([^\]]+)\]\s*(.+?)\s*\|\s*(.+?)\s*\|\s*(.+?)\s*\|$/);
  if (match) {
    sdRows.push({
      id: parseInt(match[1]),
      label: match[2],
      summary: match[3].slice(0, 200).replace(/\*\*/g, '').trim(),
      author: match[4].trim(),
      status: match[5].trim(),
    });
  }
}

const total = sdRows.length;
const latest = sdRows.slice(-20);
const first = sdRows[0];
const last = sdRows[sdRows.length - 1];

const yaml = `# session-decisions-index.yaml
# Auto-generated by bin/sd-index.js — do not edit manually
# Regenerate: node bin/sd-index.js
# Full file: docs/internal/session-decisions.md (${total} entries, append-only per SD-266)
#
# This is the BOOT file. Read this, not the full log.
# The full log is for provenance and archaeology.

generated: "${new Date().toISOString()}"
total_decisions: ${total}
range: "SD-${first?.id || '?'} to SD-${last?.id || '?'}"

recent:
${latest.map(sd => `  - id: SD-${sd.id}
    label: "${sd.label}"
    summary: "${sd.summary.replace(/"/g, '\\"')}"
    status: "${sd.status}"`).join('\n')}
`;

writeFileSync(INDEX_FILE, yaml);
console.log(`Index written: ${INDEX_FILE} (${total} total, last ${latest.length} included)`);
