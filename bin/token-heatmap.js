#!/usr/bin/env node

/**
 * token-heatmap.js — Numerical heatmap of the entire vessel, in tokens.
 *
 * Usage: node bin/token-heatmap.js [--yaml] [--boot-only]
 *
 * Outputs:
 *   - Tokens per file (sorted descending)
 *   - Tokens per directory (aggregated)
 *   - Boot sequence annotation (files read on every agent wake)
 *   - Total vessel token count
 *
 * Tokenizer: gpt-tokenizer (cl100k_base, GPT-4 compatible)
 * Not exact for Claude's tokenizer but within ~10-15%.
 * Relative ordering (which files are heavy vs light) is what matters.
 */

import { encode } from 'gpt-tokenizer';
import { execSync } from 'child_process';
import { readFileSync } from 'fs';
import { dirname, relative } from 'path';

// Boot sequence files — read on every agent wake
const BOOT_FILES = new Set([
  'AGENTS.md',
  '.claude/agents/weaver.md',
  '.claude/agents/architect.md',
  '.claude/agents/sentinel.md',
  '.claude/agents/watchdog.md',
  '.claude/agents/analyst.md',
  '.claude/agents/quartermaster.md',
  '.claude/agents/keel.md',
  '.claude/agents/scribe.md',
  '.claude/agents/janitor.md',
  '.claude/agents/maturin.md',
  '.claude/agents/anotherpair.md',
  '.claude/agents/captainslog.md',
  '.claude/agents/postcaptain.md',
  '.claude/agents/weave-quick-ref.md',
  '.opencode/agents/weaver.md',
  '.opencode/agents/architect.md',
  '.opencode/agents/sentinel.md',
  '.opencode/agents/watchdog.md',
  '.opencode/agents/analyst.md',
  '.opencode/agents/quartermaster.md',
  '.opencode/agents/keel.md',
  '.opencode/agents/scribe.md',
  '.opencode/agents/janitor.md',
  '.opencode/agents/maturin.md',
  '.opencode/agents/anotherpair.md',
  '.opencode/agents/captainslog.md',
  '.opencode/agents/postcaptain.md',
  '.opencode/agents/weave-quick-ref.md',
  'docs/internal/lexicon.md',
  'docs/internal/dead-reckoning.md',
  'docs/internal/session-decisions.md',
  '.keel-state',
]);

// Files commonly read during session (not on every boot, but frequently)
const FREQUENT_READ = new Set([
  'docs/internal/holding-deck.yaml',
  'docs/internal/estimates.yaml',
  'docs/internal/sextant.yaml',
  'docs/internal/citations.yaml',
  'docs/internal/category-one-index.yaml',
  'docs/internal/keel/producer-consumer-maps.yaml',
  'docs/internal/keel/producer-consumer-audit-trajectory.yaml',
  'docs/internal/weaver/log.md',
  'docs/internal/weaver/poa-test-suite-hardening-2026-02-28.md',
  'docs/internal/weaver/test-count-inventory.yaml',
  'slopodar.yaml',
]);

const args = process.argv.slice(2);
const yamlMode = args.includes('--yaml');
const bootOnly = args.includes('--boot-only');

// Get all tracked files
const files = execSync('git ls-files', { encoding: 'utf-8' })
  .trim()
  .split('\n')
  .filter(f => f.length > 0);

// Binary extensions to skip
const BINARY_EXT = new Set([
  '.png', '.jpg', '.jpeg', '.gif', '.ico', '.svg', '.woff', '.woff2',
  '.ttf', '.eot', '.mp4', '.webm', '.zip', '.tar', '.gz', '.br',
  '.lock', '.map',
]);

const results = [];
const dirTotals = {};
let totalTokens = 0;
let bootTokens = 0;
let frequentTokens = 0;
let skipped = 0;

for (const filepath of files) {
  const ext = filepath.slice(filepath.lastIndexOf('.'));
  if (BINARY_EXT.has(ext)) {
    skipped++;
    continue;
  }

  try {
    const content = readFileSync(filepath, 'utf-8');
    const pathTokens = encode(filepath).length;
    const contentTokens = encode(content).length;
    const total = pathTokens + contentTokens;

    const isBoot = BOOT_FILES.has(filepath);
    const isFrequent = FREQUENT_READ.has(filepath);

    results.push({
      path: filepath,
      pathTokens,
      contentTokens,
      total,
      boot: isBoot,
      frequent: isFrequent,
      lines: content.split('\n').length,
    });

    // Aggregate by directory
    const dir = dirname(filepath);
    dirTotals[dir] = (dirTotals[dir] || 0) + total;

    totalTokens += total;
    if (isBoot) bootTokens += total;
    if (isFrequent) frequentTokens += total;
  } catch {
    skipped++;
  }
}

// Sort by total tokens descending
results.sort((a, b) => b.total - a.total);

// Sort directories
const dirSorted = Object.entries(dirTotals).sort((a, b) => b[1] - a[1]);

if (bootOnly) {
  const bootFiles = results.filter(r => r.boot);
  bootFiles.forEach(f => {
    const bar = '█'.repeat(Math.ceil(f.total / 200));
    console.log(`${String(f.total).padStart(7)} tok  ${bar}  ${f.path}`);
  });
  console.log(`\nBoot total: ${bootTokens.toLocaleString()} tokens`);
  process.exit(0);
}

if (yamlMode) {
  console.log('# token-heatmap.yaml — generated by bin/token-heatmap.js');
  console.log(`# generated: ${new Date().toISOString()}`);
  console.log(`# tokenizer: gpt-tokenizer (cl100k_base)`);
  console.log(`# note: ~10-15% variance from Claude tokenizer; relative ordering is accurate`);
  console.log('');
  console.log('summary:');
  console.log(`  total_tokens: ${totalTokens}`);
  console.log(`  boot_sequence_tokens: ${bootTokens}`);
  console.log(`  frequent_read_tokens: ${frequentTokens}`);
  console.log(`  files_counted: ${results.length}`);
  console.log(`  files_skipped: ${skipped}`);
  console.log('');
  console.log('boot_sequence:');
  results.filter(r => r.boot).forEach(f => {
    console.log(`  - path: "${f.path}"`);
    console.log(`    tokens: ${f.total}`);
    console.log(`    lines: ${f.lines}`);
  });
  console.log('');
  console.log('top_50_files:');
  results.slice(0, 50).forEach(f => {
    console.log(`  - path: "${f.path}"`);
    console.log(`    tokens: ${f.total}`);
    console.log(`    path_tokens: ${f.pathTokens}`);
    console.log(`    content_tokens: ${f.contentTokens}`);
    console.log(`    lines: ${f.lines}`);
    console.log(`    boot: ${f.boot}`);
    console.log(`    frequent: ${f.frequent}`);
  });
  console.log('');
  console.log('directories:');
  dirSorted.forEach(([dir, tokens]) => {
    console.log(`  - path: "${dir}"`);
    console.log(`    tokens: ${tokens}`);
  });
  process.exit(0);
}

// Pretty-print mode (default)
const kTok = (n) => (n / 1000).toFixed(1) + 'k';
const bar = (n, max) => {
  const width = Math.ceil((n / max) * 40);
  return '█'.repeat(width) + '░'.repeat(40 - width);
};

console.log('');
console.log('═══════════════════════════════════════════════════════════════');
console.log('  TOKEN HEATMAP — THE VESSEL');
console.log('═══════════════════════════════════════════════════════════════');
console.log('');
console.log(`  Total:          ${kTok(totalTokens)} tokens across ${results.length} files`);
console.log(`  Boot sequence:  ${kTok(bootTokens)} tokens (${(bootTokens/totalTokens*100).toFixed(1)}% of vessel)`);
console.log(`  Frequent reads: ${kTok(frequentTokens)} tokens (${(frequentTokens/totalTokens*100).toFixed(1)}% of vessel)`);
console.log(`  Skipped:        ${skipped} binary/unreadable files`);
console.log('');

// Boot sequence section
console.log('───────────────────────────────────────────────────────────────');
console.log('  BOOT SEQUENCE (read on every agent wake)');
console.log('───────────────────────────────────────────────────────────────');
const bootFiles = results.filter(r => r.boot).sort((a, b) => b.total - a.total);
const maxBoot = bootFiles[0]?.total || 1;
bootFiles.forEach(f => {
  console.log(`  ${kTok(f.total).padStart(7)}  ${bar(f.total, maxBoot)}  ${f.path}`);
});
console.log(`  ${'───────'.padStart(7)}`);
console.log(`  ${kTok(bootTokens).padStart(7)}  BOOT TOTAL`);
console.log('');

// Frequent reads
console.log('───────────────────────────────────────────────────────────────');
console.log('  FREQUENT READS (commonly loaded during sessions)');
console.log('───────────────────────────────────────────────────────────────');
const freqFiles = results.filter(r => r.frequent).sort((a, b) => b.total - a.total);
const maxFreq = freqFiles[0]?.total || 1;
freqFiles.forEach(f => {
  console.log(`  ${kTok(f.total).padStart(7)}  ${bar(f.total, maxFreq)}  ${f.path}`);
});
console.log(`  ${'───────'.padStart(7)}`);
console.log(`  ${kTok(frequentTokens).padStart(7)}  FREQUENT TOTAL`);
console.log('');

// Top 30 heaviest files
console.log('───────────────────────────────────────────────────────────────');
console.log('  TOP 30 HEAVIEST FILES');
console.log('───────────────────────────────────────────────────────────────');
const maxFile = results[0]?.total || 1;
results.slice(0, 30).forEach((f, i) => {
  const tag = f.boot ? ' [BOOT]' : f.frequent ? ' [FREQ]' : '';
  console.log(`  ${String(i+1).padStart(3)}. ${kTok(f.total).padStart(7)}  ${bar(f.total, maxFile)}  ${f.path}${tag}`);
});
console.log('');

// Top 15 heaviest directories
console.log('───────────────────────────────────────────────────────────────');
console.log('  TOP 15 HEAVIEST DIRECTORIES');
console.log('───────────────────────────────────────────────────────────────');
const maxDir = dirSorted[0]?.[1] || 1;
dirSorted.slice(0, 15).forEach(([dir, tokens], i) => {
  console.log(`  ${String(i+1).padStart(3)}. ${kTok(tokens).padStart(7)}  ${bar(tokens, maxDir)}  ${dir}/`);
});
console.log('');

// Compression targets
console.log('───────────────────────────────────────────────────────────────');
console.log('  COMPRESSION TARGETS (boot files > 2k tokens)');
console.log('───────────────────────────────────────────────────────────────');
const compTargets = bootFiles.filter(f => f.total > 2000);
compTargets.forEach(f => {
  const ratio = (f.total / f.lines).toFixed(1);
  console.log(`  ${f.path}`);
  console.log(`    ${kTok(f.total)} tokens, ${f.lines} lines, ${ratio} tok/line`);
});
console.log('');
console.log('═══════════════════════════════════════════════════════════════');
